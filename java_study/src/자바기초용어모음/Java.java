package 자바기초용어모음;

// 접근제한(Access Modifiers)
// public: 어디에서든지 접근 가능 
// protected: 동일한패키지, 상속받은 자식클래스에서 접근 가능 
// default: 동일한 패키지 내에서만 접근 가능 
// private: 동일한 클래스 내에서만 접근 가능 

// static
// static 변수: 한 클래스에서 공통적인 값을 사용할때 사용  
// static 메서드: 인스턴스 생성 없이 클래스 이름을 통해 직접 호출 가능 

// final
// final 변수: 상수, 선언 시 반드시 초기화 
// final 메서드: 오버라이딩 불가능 
// final 클래스: 상속 불가능 

// abstract
// abstract 클래스: 추상 클래스, 하나 이상의 추상 메서드를 포함 
// abstract 메서드: 추상메서드, 구현부가 없다.

// interface VS abstract class
// interface: 다중 구현이 가능 
// abstract class: 다중 상속이 불가능, 추상 메서드 이외에 일반적인 메서드를 가질수 있다.

// extends VS implements 
// extends: 확장하다. 상속 관계시 사용 
// implements: 구현. 인터페이스를 구현 시 사용 

// super VS this
// super: 상속받은 클래스에서 부모 클래스를 가리킴 
// this: 자기 자신을 가르킨다. 

// 오버로딩(Overloading) VS 오버라이딩(Overriding)
// 오버로딩: 동일한 이름의 메서드의 매개변수의 수 또는 자료형이 다른 것 
// 오버라이딩: 부모 클래스의 메서드를 "재정의"

// try-catch-finally
// try: 오류가 날 수 있는 코드를 작성 
// catch: 오류가 발생시 catch 에서 잡아서 예외처리 
// finally: catch 에 잡히든 잡히지 않든 무조건 실행되는 코드 

